//Write a function named greet that takes a name as an argument and prints "Hello, [name]!"

function greet(name) {
    console.log(`Hello! ${name}`); // =>  Hello! Neeraj
}

greet("Neeraj");


// Arrow function
const greeting = (name) => {
    console.log("Hello " + name); // =>  Hello! Arshdeep
}

greeting("Arshdeep");